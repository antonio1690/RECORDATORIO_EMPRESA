package Java;

public class Caracteristicas {
    /*
     * Características Fundamentales
     * Fuertemente tipado.
     * Java es un lenguaje orientado a objetos, independiente de plataforma
     * ("Write Once, Run Anywhere"), robusto, seguro y con soporte para multihilos,
     * destaca por tener alto rendimiento gracias al compilador JIT y por su extensa
     * comunidad.
     * 
     * Principios de Programación Orientada a Objetos
     * Es esencial dominar principios como:
     * 
     * Encapsulamiento:
     * Limitar el acceso a los datos internos de una clase.
     * 
     * Herencia: reutilizar funcionalidades mediante clases derivadas.
     * 
     * Polimorfismo: referenciar objetos de distintas subclases usando una
     * superclase.
     * 
     * Abstracción: definir interfaces o clases abstractas que ocultan detalles de
     * implementación.
     * 
     * Conocimientos sobre Colecciones y Estructuras de Datos
     * Preguntas frecuentes incluyen diferencias entre:
     * 
     * Array vs ArrayList (Array es de tamaño fijo, ArrayList dinámico).
     * 
     * ArrayList vs Vector (Vector es sincronizado, ArrayList no).
     * 
     * HashSet, LinkedList y otras implementaciones del Java Collections Framework.
     * 
     * Conceptos Avanzados: Streams, Lambdas y Concurrencia
     * Java 8 introdujo expresiones lambda y el API de Streams para facilitar la
     * programación funcional y el procesamiento eficiente de datos.
     * 
     * Saber diferenciar entre Stream secuencial y paralelo.
     * 
     * Explicar interfaces funcionales y referencias de métodos (method references).
     * 
     * Manejo de Excepciones y JVM Internals
     * Debes saber manejar excepciones (try-catch-finally), tipos de errores, y
     * diferencias entre errores personalizados y de sistema. Además, explicar el
     * funcionamiento de la JVM: cómo ejecuta bytecode, para qué sirve el
     * ClassLoader.
     * 
     * Frameworks y Ecosistema Java
     * Las empresas suelen preguntar por frameworks como Spring Boot, Hibernate, JPA
     * y preguntas sobre patrones de diseño (Singleton, Factory, DAO). Saber
     * explicar el rol de Spring Boot como simplificador del desarrollo Java full
     * stack y qué facilidades aporta.
     * 
     * Bases de Datos y Acceso a Datos
     * Preguntas típicas sobre JDBC, JPA, diferencias entre java.util.Date y
     * java.sql.Date, y la integración con bases de datos relacionales y no
     * relacionales (MongoDB, Redis).
     * 
     * Temas Complementarios que Conviene Repasar
     * Arquitecturas monolíticas vs. microservicios.
     * 
     * Principios SOLID y patrones de diseño.
     * 
     * Seguridad (autenticación JWT, protección contra inyección de SQL).
     * 
     * Conceptos de programación reactiva con Spring WebFlux.
     */
}
